package examen.e2;

public class Poliza {
	String nombre; 
	String descripcion; 
	int anioVigencia; 
	String codigoPoliza; 
	int valorPoliza;
	
	String getNombre() {
		return nombre;
	}
	void setNombre(String nombre) {
		this.nombre = nombre;
	}
	String getDescripcion() {
		return descripcion;
	}
	void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	int getAnioVigencia() {
		return anioVigencia;
	}
	void setAnioVigencia(int anioVigencia) {
		this.anioVigencia = anioVigencia;
	}
	String getCodigoPoliza() {
		return codigoPoliza;
	}
	void setCodigoPoliza(String codigoPoliza) {
		this.codigoPoliza = codigoPoliza;
	}
	int getValorPoliza() {
		return valorPoliza;
	}
	void setValorPoliza(int valorPoliza) {
		this.valorPoliza = valorPoliza;
	} 
	
	


}
